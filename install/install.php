        $sql = file_get_contents($base . '/database/schema.sql');
        // 移除 DEFINER/FOREIGN_KEY 等
        $sql = preg_replace('/SET FOREIGN_KEY_CHECKS = [01];/i', '', $sql);
        $pdo->exec($sql);

        // 写入 .env
