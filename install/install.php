        $envContent .= "APP_TIMEZONE=" . ($_POST['timezone'] ?? 'Asia/Shanghai') . "\n";
        $envContent .= "APP_KEY=$appKey\n\n";
        $envContent .= "DB_HOST=$host\n";
        $envContent .= "DB_PORT=$port\n";
        $envContent .= "DB_DATABASE=$name\n";
        $envContent .= "DB_USERNAME=$user\n";
        $envContent .= "DB_PASSWORD=$pass\n";
        $envContent .= "DB_CHARSET=utf8mb4\n\n";
        $envContent .= "ADMIN_PATH=$adminPath\n";
        $envContent .= "ADMIN_PORT=$adminPort\n";
        $envContent .= "WEB_PORT=80\n\n";
        $envContent .= "MAIL_HOSTNAME=" . ($_POST['mail_hostname'] ?? 'mail.local') . "\n";
        $envContent .= "LOG_PATH=$base/logs\n";

        file_put_contents($base . '/.env', $envContent);

        // 创建管理员
        $adminUser = trim($_POST['admin_user'] ?? 'admin');
        $adminPass = $_POST['admin_pass'] ?? '';
        $adminEmail= trim($_POST['admin_email'] ?? 'admin@localhost');
        $pdo->prepare("UPDATE ms_users SET username = ?, password = ?, email = ?, display_name = '系统管理员' WHERE username = 'admin'")
            ->execute([$adminUser, password_hash($adminPass, PASSWORD_DEFAULT), $adminEmail]);

        // 更新设置
        $pdo->prepare("UPDATE ms_settings SET `value` = ? WHERE key_name = 'admin_path'")->execute([$adminPath]);
        $pdo->prepare("UPDATE ms_settings SET `value` = ? WHERE key_name = 'admin_port'")->execute([$adminPort]);
        $pdo->prepare("UPDATE ms_settings SET `value` = ? WHERE key_name = 'mail_hostname'")->execute([$_POST['mail_hostname'] ?? 'mail.local']);
        $pdo->prepare("UPDATE ms_settings SET `value` = ? WHERE key_name = 'site_name'")->execute(['MailSystem']);

        @mkdir($base . '/data/mailboxes', 0755, true);
        @mkdir($base . '/storage/cache', 0755, true);
        @mkdir($base . '/storage/sessions', 0755, true);
        @mkdir($base . '/logs', 0755, true);

        // 锁定
        file_put_contents($lockFile, json_encode([
            'installed_at' => date('Y-m-d H:i:s'),
            'version' => '1.0.0',
        ]));

        $_SESSION['install_success'] = true;
        header('Location: /install/install.php?step=3');
        exit;
    } catch (Throwable $e) {
        $err = $e->getMessage();
    }
}
