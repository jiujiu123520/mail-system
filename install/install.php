function checkEnvironment() {
    $checks = [];
    $checks[] = ['PHP >= 7.4',     version_compare(PHP_VERSION, '7.4.0', '>='), PHP_VERSION];
    $checks[] = ['PDO MySQL',      extension_loaded('pdo_mysql'), '必需'];
    $checks[] = ['OpenSSL',        extension_loaded('openssl'), 'SSL 端口需要'];
    $checks[] = ['mbstring',       extension_loaded('mbstring'), 'MIME 解析'];
    $checks[] = ['iconv',          extension_loaded('iconv'), '编码转换'];
    $checks[] = ['Sockets (可选)', extension_loaded('sockets'), '高性能 socket'];
    $checks[] = ['pcntl (可选)',   extension_loaded('pcntl'), '进程管理'];
    $checks[] = ['storage 目录可写', is_writable($base . '/storage'), $base . '/storage'];
    $checks[] = ['data 目录可写',    is_writable($base . '/data') || @mkdir($base . '/data', 0755, true), $base . '/data'];
    $checks[] = ['config 目录可写',  is_writable($base . '/config'), $base . '/config'];
    return $checks;
}

